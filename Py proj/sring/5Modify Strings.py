




a = "Hello, World!"
print(a.upper())                  #ทำให้เป็นตัวพิมพ์ใหญ่ทุกตัว



a = "Hello, World!"
print(a.lower())                  #ทำให้เป็นตัวพิมพ์เล็กทุกตัว




a = "ใใใใ    Hello, World!   "
print(a.strip())                  #ลบช่องวางก่อนเริ่มและจบ




a = "Hello, World!"
print(a.replace("o", "J"))        #กาารแทนที่ด้วยอีกตัว




a = "Hello, World!"
print(a.split(","))              # returns ['Hello', ' World!']